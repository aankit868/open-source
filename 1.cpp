#include<bits/stdc++.h>
using namespace std;
template<typename... T>
void read(T&... args)
{
    ((cin>>args), ...);
}
template<typename... T>
void write(T... args)
{
    ((cout<<args<<" "), ...);
}
int main()
{
   int a,b,c;
   string s1,s2;
   read(a,b,c,s1,s2);
   write(a,b,c,s1,s2);
}
